active_bites = {}

async def bite_loop(client, target_id, chat_id, stop_event):
    while not stop_event.is_set():
        for insult in insults:
            if stop_event.is_set():
                break
            try:
                await client.send_message(chat_id, f'@{target_id} {insult}')
                await asyncio.sleep(0.1)
            except FloodWaitError as e:
                await asyncio.sleep(e.seconds)
            except:
                pass

client = TelegramClient(SESSION_NAME, API_ID, API_HASH)

@client.on(events.NewMessage)
async def handler(event):
    if event.sender_id == (await client.get_me()).id:
        return
    
    text = event.raw_text.strip()
    chat = event.chat_id

    if not text.startswith(PREFIX):
        return
    
    cmd = text[len(PREFIX):].split()[0].lower()
    
    if cmd == 'пинг':
        start = time.time()
        msg = await event.reply('🏓 Понг...')
        await msg.edit(f'🏓 Понг! {round((time.time()-start)*1000)}ms')
        return

    if cmd == 'байт' and event.is_reply:
        target_msg = await event.get_reply_message()
        target_id = target_msg.sender_id
        key = f"{chat}_{target_id}"
        
        if key not in active_bites:
            stop_event = asyncio.Event()
            task = asyncio.create_task(bite_loop(client, target_id, chat, stop_event))
            active_bites[key] = (task, stop_event)
            await event.reply('⚔️ Байтинг начат')
        else:
            await event.reply('❌ Байтинг уже активен')
        return

    if cmd == 'байтстоп':
        removed = False
        for key in list(active_bites.keys()):
            if key.startswith(f"{chat}_"):
                task, stop_event = active_bites[key]
                stop_event.set()
                task.cancel()
                del active_bites[key]
                removed = True
        if removed:
            await event.reply('🛑 Байтинг остановлен')
        else:
            await event.reply('❌ Нет активного байтинга')
        return
        
    if cmd == 'команды':
        await event.reply('📋 .байт (ответ), .байтстоп, .пинг')
        return

