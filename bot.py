active_sends = {}  # Для хранения активных задач

async def send_all_insults(client, target, chat_id, event, key):
    """Фоновая задача для отправки всех оскорблений"""
    try:
        message_lines = []
        for insult in insults:
            message_lines.append(f'@{target} {insult}')
        full_message = '\n'.join(message_lines)
        
        # Отправляем всё одним сообщением
        await client.send_message(chat_id, full_message)
        
        # Если задача не была остановлена, редактируем сообщение об успехе
        if key in active_sends and active_sends[key] is not None:
            await event.edit(f'✅ Отправлено {len(insults)} оскорблений')
    except Exception as e:
        if key in active_sends:
            await event.edit(f'❌ Ошибка: {e}')
    finally:
        # Удаляем задачу из словаря после завершения
        if key in active_sends:
            del active_sends[key]

async def main():
    client = TelegramClient(SESSION_NAME, API_ID, API_HASH)
    await client.start()
    me = await client.get_me()
    print(f'\n✅ Аккаунт: {me.first_name} (@{me.username})')
    print('📋 .ку @username - отправить все оскорбления')
    print('📋 .кустоп - остановить отправку\n')
    
    @client.on(events.NewMessage)
    async def handler(event):
        if event.sender_id == me.id:
            return
        
        text = event.raw_text.strip()
        chat = event.chat_id
        
        if not text.startswith(PREFIX):
            return
        
        parts = text[len(PREFIX):].split()
        if not parts:
            return
        cmd = parts[0].lower()
        
        # КОМАНДА .ку @username
        if cmd == 'ку' and len(parts) > 1:
            target = parts[1].replace('@', '')
            key = f"{chat}_{target}"
            
            # Если уже есть активная отправка для этого чата и цели
            if key in active_sends and active_sends[key] is not None:
                await event.edit('⚠️ Уже отправляю, подожди или останови командой .кустоп')
                return
            
            try:
                # Проверяем, существует ли пользователь
                await client.get_entity(target)
                
                # Редактируем сообщение с командой
                await event.edit('⏳ Отправляю все оскорбления...')
                
                # Запускаем фоновую задачу
                task = asyncio.create_task(send_all_insults(client, target, chat, event, key))
                active_sends[key] = task
                
            except Exception as e:
                await event.edit(f'❌ Не найден пользователь: {target}')
            return
        
        # КОМАНДА .кустоп - остановить отправку
        if cmd == 'кустоп':
            key_to_remove = None
            for key in list(active_sends.keys()):
                if key.startswith(f"{chat}_"):
                    key_to_remove = key
                    break
            
            if key_to_remove:
                task = active_sends[key_to_remove]
                task.cancel()
                del active_sends[key_to_remove]
                await event.edit('🛑 Отправка остановлена')
            else:
                await event.edit('❌ Нет активной отправки')
            return
        
        if cmd == 'пинг':
            start = time.time()
            msg = await event.reply('🏓 Понг...')
            await msg.edit(f'🏓 Понг! {round((time.time()-start)*1000)}ms')
            return
    
    await client.run_until_disconnected()

asyncio.run(main())